    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        String nombre = txtNombre.getText();
        String NIT = txtNIT.getText();
        String tel = txtTelefono.getText();
        String dir = txtDireccion.getText();
        String email = txtEmail.getText();
        String actEconomica = txtActEconomica.getText();

        // Compara si todos los campos estan vacios
        boolean comp1 = nombre.equals("") || NIT.equals("") || actEconomica.equals("");
        boolean comp2 = tel.equals("") || dir.equals("") || email.equals("");

        if ( comp1 || comp2 ){
            JOptionPane.showMessageDialog(null, "Debe diligenciar todos los campos.");
        } else {
            Empresa empresa = new Empresa(nombre, NIT, tel, dir, email, actEconomica);
            JOptionPane.showMessageDialog(null, "Datos almacenados exitosamente.");
            limpiarCampos();
        }
    }//GEN-LAST:event_btnGuardarActionPerformed


    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed

        String nombre = txtNombre.getText().toUpperCase();
        String NIT = txtNIT.getText();
        String tel = txtTelefono.getText();
        String dir = txtDireccion.getText().toUpperCase();
        String email = txtEmail.getText();
        String actEconomica = txtActEconomica.getText().toUpperCase();

        // Compara si todos los campos son iguales a los existentes
        boolean comp1 = nombre.equals("") || NIT.equals("") || actEconomica.equals("");
        boolean comp2 = tel.equals("") || dir.equals("") || email.equals("");

        int fila = tablaEmpresa.getSelectedRow();
        if (fila == -1) {

            JOptionPane.showMessageDialog(null, "Seleccionar el registro de la tabla");
        } else {
            if (comp1 || comp2) {
                JOptionPane.showMessageDialog(null, "La información no registra ningún cambio.");
            } else {                
                empresa = new Empresa(nombre, NIT, tel, dir, email, actEconomica);
                limpiarTabla(modelo);
                listar();
                disenoTabla();
                JOptionPane.showMessageDialog(null, "Datos actualizados exitosamente.");
                limpiarCampos();
                inhabilitarCampos();
            }
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed

        String nombre = txtNombre.getText().toUpperCase();
        String NIT = txtNIT.getText();
        String tel = txtTelefono.getText();
        String dir = txtDireccion.getText().toUpperCase();
        String email = txtEmail.getText();
        String actEconomica = txtActEconomica.getText().toUpperCase();

        // Compara si todos los campos estan vacios
        boolean comp1 = nombre.equals("") || NIT.equals("") || actEconomica.equals("");
        boolean comp2 = tel.equals("") || dir.equals("") || email.equals("");

        if (comp1 || comp2) {
            JOptionPane.showMessageDialog(null, "Debe diligenciar todos los campos.");
        } else {            
            empresa = new Empresa(nombre, NIT, tel, dir, email, actEconomica);
            JOptionPane.showMessageDialog(null, "Datos almacenados exitosamente.");
            listar();
            disenoTabla();
            habilitarbotones();
            limpiarCampos();
            inhabilitarCampos();
        }
    }//GEN-LAST:event_btnAgregarActionPerformed


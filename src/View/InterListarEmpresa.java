    private void tablaEmpresaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaEmpresaMouseClicked
        int fila = tablaEmpresa.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(null, "Seleccionar el registro de la tabla");
            btnActualizar.setEnabled(false);
            btnEliminar.setEnabled(false);
            btnVer.setEnabled(false);
        } else {
            habilitarCampos();
            String nom = (String) tablaEmpresa.getValueAt(fila, 0);
            String NIT = (String) tablaEmpresa.getValueAt(fila, 1);
            String tel = (String) tablaEmpresa.getValueAt(fila, 2);
            String dir = (String) tablaEmpresa.getValueAt(fila, 3);
            String email = (String) tablaEmpresa.getValueAt(fila, 4);
            String actEcon = (String) tablaEmpresa.getValueAt(fila, 5);

            txtNombre.setText(nom);
            txtNIT.setText(NIT);
            txtTelefono.setText(tel);
            txtDireccion.setText(dir);
            txtEmail.setText(email);
            txtActEconomica.setText(actEcon);
        }
    }//GEN-LAST:event_tablaEmpresaMouseClicked

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int fila = tablaEmpresa.getSelectedRow();
        if (fila < 0) {
            JOptionPane.showMessageDialog(null, "Seleccionar el registro de la tabla");

        } else {
            inhabilitarCampos();
            int op = JOptionPane.showConfirmDialog(null, "Esta seguro de eliminar el registro?", "Advertencia", JOptionPane.YES_NO_OPTION, JOptionPane.ERROR_MESSAGE);
            if (op == JOptionPane.YES_OPTION) {
                JOptionPane.showMessageDialog(null, "Registro eliminado exitosamente.");
                limpiarTabla(modelo);
                limpiarCampos();
                habilitarCampos();
            } else {                
                listar();
                disenoTabla();
                limpiarCampos();
                inhabilitarCampos();
            }

        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void txtNITKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNITKeyTyped
       char car = evt.getKeyChar();
       //Permite validar que solo se ingresen datos numéricos.
       if((car < '0' || car > '9') && (car != (char)KeyEvent.VK_BACK_SPACE) && (car != (char)KeyEvent.VK_DELETE)){
           evt.consume();
           JOptionPane.showMessageDialog(null, "Solo se admiten números. Verifique su información.");           
       }  
    }//GEN-LAST:event_txtNITKeyTyped

